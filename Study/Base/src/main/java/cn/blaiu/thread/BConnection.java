	@Override
	public void close() throws SQLException {
		synchronized (this) {
			Connection c = conn;
			conn = null;
			if (null != c) {
				try {
					c.rollback();
				} catch (Exception e) {
					e.printStackTrace();
				}
				pool.release(c);
			}
		}
	}
	
	/**
	 * 关闭jdbc真实链接
	 * @throws SQLException
	 */
	public void closeJdbcConnection() throws SQLException {
		if(null != conn) {
			conn.close();
		}
		conn = null;
	}
	
	public void closeConnectionAndRelease() throws SQLException {
		synchronized (this) {
			Connection c = conn;
			conn = null;
			if(null != c) {
				try {
					c.close();
				} catch (Exception e) {
					e.printStackTrace();
				}
			}
			pool.newConnection(c);
		}
	}

